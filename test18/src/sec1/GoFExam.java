package sec1;

public class GoFExam {

	public static void main(String[] args) {
		// 생성(Creational) 패턴: 객체(Instance) 생성에 관련된 패턴으로 객체가 생성되거나 변경되어도 프로그램 구조에 영향을 끼치지 않도록 유연성과 재사용성을 향상시킨 디자인 패턴
		// 구조(Structural) 패턴: 여러 클래스를 혼합하기 위한 자바 디자인 패턴. 객체와 클래스를 보다 큰 구조로 조립하여 유연하고, 효율적인 방법을 제공하는 패턴
		// 행위(Behavior) 패턴: 메소드와 관련하여 기능을 분해하거나 조합하기 위한 자바 디자인 패턴.
		System.out.println("GoF");
		System.out.println("생성패턴");
		System.out.println("구조패턴");
		System.out.println("행위패턴");
	}

}
